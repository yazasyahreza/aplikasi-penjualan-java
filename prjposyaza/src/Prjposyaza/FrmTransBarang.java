    //Perintah menampilkan nomor otomatis
    public void buatnomor(){
        try {
            ResultSet rs = db.ambilData("Select no_trans as auto from ttrans_jual ORDER by no_trans desc");
            
            if (rs.next()) {
                int no_u = Integer.parseInt(rs.getString("auto")) + 1;
                jno_trans.setText(Integer.toString(no_u));
            } else {
                int no_t = 1;
                jno_trans.setText(Integer.toString(no_t));
            }
            rs.close();
        } catch (Exception e) {
        }
    }
    
