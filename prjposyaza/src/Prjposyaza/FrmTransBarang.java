    private void jkd_brgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jkd_brgActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rs = db.ambilData("select * from tbarang where kd_brg='" + jkd_brg.getText() + "'");
            if (rs.next()) {
                jnama_brg.setText(rs.getString("nama_brg"));
                jharga.setText(rs.getString("harga"));
                jstok.setText(rs.getString("jml"));
                jjml.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Kode Belum Terdaftar");
                jkd_brg.selectAll();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jkd_brgActionPerformed

