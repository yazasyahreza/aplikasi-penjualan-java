    private void jtabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtabelMouseClicked
        // TODO add your handling code here:
        try {
            int n = JOptionPane.showConfirmDialog(this, "Anda yakin membatalkan item ini?", "RPL Message",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE, null);
            if (n == JOptionPane.YES_OPTION){
                model.removeRow(jtabel.getSelectedRow());
                int row = jtabel.getRowCount();
                int tot = 0, hrg, jml;
                for (int i = 0; i < row; i++){
                    hrg = Integer.parseInt(jtabel.getValueAt(i, 2).toString());
                    jml = Integer.parseInt(jtabel.getValueAt(i, 3).toString());
                    tot += (hrg * jml);
                }
                jtotal.setText(String.valueOf(tot));
            }else if(n == JOptionPane.NO_OPTION){
                return;
            }else{
                return;
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jtabelMouseClicked

